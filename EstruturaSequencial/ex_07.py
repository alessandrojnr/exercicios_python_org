# 7 - Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
"""Sabendo que a formula do quadrado é de a= l² vamos ao código"""
lado = int(input("Qual é o tamano do lado do quadrado: "))
area = lado**2
print(f'A área do quadrado é de {area} já o dobro desta área é de {area*2}')
